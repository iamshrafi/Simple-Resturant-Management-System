   public void validateFields() {
    String name = txtName.getText();
    String dob = txtDob.getText();
    String mobileNumber = txtMobileNumber.getText();
    String email = txtEmail.getText();
    String address = txtAddress.getText();
    String secQue = txtSecurityQuestion.getText();
    String answer = txtAnswer.getText();
    String userName = txtUserName.getText();
    String password = txtPassword.getText();
    
   

    if (!name.isEmpty() && !dob.isEmpty() && !mobileNumber.isEmpty() && mobileNumber.length() == 11 && mobileNumber.matches(mobileNumberPattern) && !email.isEmpty() && email.matches(emailPattern) && !address.isEmpty() && !secQue.isEmpty() && !answer.isEmpty() && !userName.isEmpty() && !password.isEmpty()) {
        btnSubmit.setEnabled(true);
    } else {
        btnSubmit.setEnabled(false);
    }
}


