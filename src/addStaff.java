    private void txtUserNameKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUserNameKeyReleased
        // TODO add your handling code here:
        String userName = txtUserName.getText();

        // Check if the username is not empty
        if (!userName.equals("")) {
            try {
                // Check if the username is taken
                boolean isTaken = isUserNameTaken(userName);

                // If the username is taken, display a message
                if (isTaken) {
                    JOptionPane.showMessageDialog(null, "Username already exists");
                }

                // You can add more logic here if needed
            } catch (Exception e) {
                // Handle any exceptions that might occur
                JOptionPane.showMessageDialog(null, e);
            }
        }
         validateFields();
    }//GEN-LAST:event_txtUserNameKeyReleased

    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        User user = new User();
        user.setName(txtName.getText());
        user.setDob(txtDob.getText());
        user.setMobileNumber(txtMobileNumber.getText());
        user.setEmail(txtEmail.getText());
        user.setUserName(txtUserName.getText());
        user.setPassword(txtPassword.getText());
        user.setAddress(txtAddress.getText());
        user.setSecurityQuestion(txtSecurityQuestion.getText());
        user.setAnswer(txtAnswer.getText());

        if (checkUsername == 1) {
            JOptionPane.showMessageDialog(null, "Username is taken");
        }

        UserDao.save(user);
        setVisible(false);
        new addStaff().setVisible(true);
    }//GEN-LAST:event_btnSubmitActionPerformed

