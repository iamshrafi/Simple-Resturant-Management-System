     public void getAllRecords() {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        ArrayList<Medicine> list = MedicineDao.getAllRecords();
        Iterator<Medicine> itr = list.iterator();
        while (itr.hasNext()) {
            Medicine medicineObj = itr.next();
            dtm.addRow(new Object[]{medicineObj.getId(),medicineObj.getUniqueId(), medicineObj.getName(), medicineObj.getCompanyName(), medicineObj.getQuantity(), medicineObj.getPrice()});
        }
    }
    
